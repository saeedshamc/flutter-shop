  // محصولات ویژه
  static List<ProductModel> getFeaturedProducts() {
    return getProducts().where((p) => p.isFeatured).toList();
  }

  // جدیدترین محصولات
  static List<ProductModel> getNewProducts() {
    final products = getProducts();
    products.sort((a, b) => (b.createdAt ?? DateTime.now()).compareTo(a.createdAt ?? DateTime.now()));
    return products.take(6).toList();
  }

  // محصولات بر اساس دسته‌بندی
  static List<ProductModel> getProductsByCategory(String categoryId) {
    return getProducts().where((p) => p.categoryId == categoryId).toList();
  }
